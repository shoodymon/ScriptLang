def is_vowel(char):
    return char.lower() in 'аеёиоуыэюя'

poem = """Серые глаза – рассвет,
Пароходная сирена,
Дождь, разлука, серый след
За винтом бегущей пены.

Чёрные глаза – жара,
В море сонных звёзд скольженье,
И у борта до утра
Поцелуев отраженье.

Синие глаза – луна,
Вальса белое молчанье,
Ежедневная стена
Неизбежного прощанья.

Карие глаза – песок,
Осень, волчья степь, охота,
Скачка, вся на волосок
От паденья и полёта.

Нет, я не судья для них,
Просто без суждений вздорных
Я четырежды должник
Синих, серых, карих, чёрных.

Как четыре стороны
Одного того же света,
Я люблю – в том нет вины –
Все четыре этих цвета."""

with open("task_52_poem.txt", "w", encoding="utf-8") as file:
    file.write(poem)

with open("task_52_poem.txt", "r", encoding="utf-8") as file:
    poem_text = file.read()
    print("Стихотворение:")
    print(poem_text)

words = poem_text.split()
# Генератор списка и функция sum() для подсчета слов, начинающихся с гласной
# 1. (word for word in words) - перебирает все слова в списке
# 2. if is_vowel(word[0]) - проверяет, является ли первая буква слова гласной
# 3. sum() считает количество True результатов (т.е. слов, начинающихся с гласной)
vowel_count = sum(1 for word in words if is_vowel(word[0]))
consonant_count = len(words) - vowel_count

print(f"\nСлов, начинающихся с гласной: {vowel_count}")
print(f"Слов, начинающихся с согласной: {consonant_count}")

if vowel_count > consonant_count:
    print("В стихотворении больше слов, начинающихся на гласную.")
elif consonant_count > vowel_count:
    print("В стихотворении больше слов, начинающихся на согласную.")
else:
    print("В стихотворении одинаковое количество слов, начинающихся на гласную и согласную.")
